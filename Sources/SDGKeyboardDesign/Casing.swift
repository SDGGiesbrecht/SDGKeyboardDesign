internal func autoUppercase(_ lowercase: [Key: StrictString]) -> [Key: StrictString] {
  var result = [Key: StrictString]()
  for (key, output) in lowercase {
    if let uppercase = basicCasing[output] {
      result[key] = uppercase
    }
  }
  return result
}
